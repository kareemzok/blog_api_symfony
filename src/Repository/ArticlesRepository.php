    public function searchForArticles($keyword): array
    {
        // below is for exact match
        return $this->createQueryBuilder('a')
            ->orWhere('a.title = :title')
            ->orWhere('a.body = :body')
            ->setParameter('title', $keyword)
            ->setParameter('body', $keyword)
            ->getQuery()
            ->getResult()
        ;
    }


